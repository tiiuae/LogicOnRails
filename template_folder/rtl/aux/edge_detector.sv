
module edge_detector (
    input  logic i_clk,
    input  logic i_rst_n,
    input  logic i_data,
    output logic o_data
);

/////////////////////////////////////////////////////////////////////////////////////
// signals                                                                         //
/////////////////////////////////////////////////////////////////////////////////////

logic r_toggle;

/////////////////////////////////////////////////////////////////////////////////////
// edge                                                                           //
/////////////////////////////////////////////////////////////////////////////////////

always_ff @(posedge i_clk or negedge i_rst_n)
begin
    if (i_rst_n == 1'b0) begin
        r_toggle <= 1'b0;
    end else begin
        r_toggle <= i_data;
    end
end

/////////////////////////////////////////////////////////////////////////////////////
// output                                                                          //
/////////////////////////////////////////////////////////////////////////////////////

assign o_data = r_toggle ^ i_data;

endmodule